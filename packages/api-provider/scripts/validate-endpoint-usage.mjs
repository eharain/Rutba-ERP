#!/usr/bin/env node
/**
 * validate-endpoint-usage.mjs
 *
 * Hard CI gate that catches the runtime error class:
 *   "<Endpoint>Endpoints.<typoMethod> is not a function"
 *
 * Strategy:
 *   1. Walk providers/generated/client/**\/*.d.ts and parse each
 *      `export interface <Name>EndpointsType { ... }` declaration to build
 *      a map { EndpointName -> Set<allowedMemberNames> }.
 *   2. Walk consumer apps (every sibling of packages/api-provider that
 *      imports from @rutba/api-provider) and grep for
 *      `<EndpointName>.<member>(` access patterns.
 *   3. For each access, if the member is not in the allowed set, record it
 *      as a violation.
 *   4. Print all violations grouped by file and exit non-zero on any.
 *
 * The .d.ts files are the source of truth — they are regenerated by
 * scaffold-endpoint-providers.mjs from the /api descriptors on every build,
 * so this validator is automatically aligned with whatever the scaffolder
 * produced.
 */

import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

const packageRoot = path.resolve(__dirname, '..');
const monorepoRoot = path.resolve(packageRoot, '..', '..');
const generatedClientDir = path.join(packageRoot, 'providers', 'generated', 'client');
const endpointsIndexDts = path.join(packageRoot, 'endpoints', 'index.d.ts');

const CONSUMER_GLOB_DIRS = [
    'rutba-rider',
    'rutba-cms',
    'rutba-order-management',
    'rutba-social',
    'rutba-web',
    'pos-auth',
    'pos-sale',
    'pos-stock',
    'packages/pos-shared',
    'packages/api-provider/endpoints',
    'packages/api-provider/pos',
];

const FILE_EXT_INCLUDE = new Set(['.js', '.jsx', '.ts', '.tsx', '.mjs', '.cjs']);
const DIR_EXCLUDE = new Set([
    'node_modules',
    '.next',
    'dist',
    'build',
    '.git',
    'temp',
    'providers',
    'generated',
]);

const INTERFACE_HEADER_REGEX = /export\s+interface\s+([A-Za-z_$][\w$]*)Type\s*{/g;
const CONST_OBJECT_HEADER_REGEX = /export\s+const\s+([A-Za-z_$][\w$]*Endpoints)\s*:\s*{/g;
const MEMBER_LINE_REGEX = /^\s*(?:'([^']+)'|"([^"]+)"|([A-Za-z_$][\w$]*))\s*\??\s*[(:]/gm;

function extractBalancedBraceBody(source, openBraceIndex) {
    if (source[openBraceIndex] !== '{') return null;
    let depth = 0;
    let inSingle = false;
    let inDouble = false;
    let inTemplate = false;
    let inLineComment = false;
    let inBlockComment = false;
    let escaped = false;

    for (let i = openBraceIndex; i < source.length; i += 1) {
        const ch = source[i];
        const next = source[i + 1];

        if (inLineComment) {
            if (ch === '\n') inLineComment = false;
            continue;
        }
        if (inBlockComment) {
            if (ch === '*' && next === '/') {
                inBlockComment = false;
                i += 1;
            }
            continue;
        }
        if (escaped) { escaped = false; continue; }
        if (inSingle || inDouble || inTemplate) {
            if (ch === '\\') { escaped = true; continue; }
            if (inSingle && ch === "'") inSingle = false;
            else if (inDouble && ch === '"') inDouble = false;
            else if (inTemplate && ch === '`') inTemplate = false;
            continue;
        }

        if (ch === '/' && next === '/') { inLineComment = true; i += 1; continue; }
        if (ch === '/' && next === '*') { inBlockComment = true; i += 1; continue; }
        if (ch === "'") { inSingle = true; continue; }
        if (ch === '"') { inDouble = true; continue; }
        if (ch === '`') { inTemplate = true; continue; }
        if (ch === '{') depth += 1;
        else if (ch === '}') {
            depth -= 1;
            if (depth === 0) return source.slice(openBraceIndex + 1, i);
        }
    }

    return null;
}

function parseMemberNames(body) {
    const members = new Set();
    MEMBER_LINE_REGEX.lastIndex = 0;
    let m;
    while ((m = MEMBER_LINE_REGEX.exec(body)) !== null) {
        const name = m[1] ?? m[2] ?? m[3];
        if (name) members.add(name);
    }
    return members;
}

function readGeneratedMemberSets() {
    const result = new Map();

    function walk(dir) {
        const entries = fs.readdirSync(dir, { withFileTypes: true });
        for (const entry of entries) {
            const full = path.join(dir, entry.name);
            if (entry.isDirectory()) {
                walk(full);
                continue;
            }
            if (!entry.name.endsWith('.d.ts') || entry.name === 'index.d.ts') continue;
            const source = fs.readFileSync(full, 'utf8');
            INTERFACE_HEADER_REGEX.lastIndex = 0;
            let m;
            while ((m = INTERFACE_HEADER_REGEX.exec(source)) !== null) {
                const exportName = m[1];
                const openBraceIndex = m.index + m[0].length - 1;
                const body = extractBalancedBraceBody(source, openBraceIndex);
                if (body === null) continue;
                const members = parseMemberNames(body);
                if (members.size === 0) continue;

                const existing = result.get(exportName);
                if (existing) {
                    members.forEach((mem) => existing.add(mem));
                } else {
                    result.set(exportName, members);
                }
            }
        }
    }

    walk(generatedClientDir);

    if (fs.existsSync(endpointsIndexDts)) {
        const source = fs.readFileSync(endpointsIndexDts, 'utf8');
        CONST_OBJECT_HEADER_REGEX.lastIndex = 0;
        let m;
        while ((m = CONST_OBJECT_HEADER_REGEX.exec(source)) !== null) {
            const name = m[1];
            const openBraceIndex = m.index + m[0].length - 1;
            const body = extractBalancedBraceBody(source, openBraceIndex);
            if (body === null) continue;
            const members = parseMemberNames(body);
            if (members.size === 0) continue;

            const existing = result.get(name);
            if (existing) {
                members.forEach((mem) => existing.add(mem));
            } else {
                result.set(name, members);
            }
        }
    }

    return result;
}

function collectConsumerFiles() {
    const files = [];

    function walk(dir) {
        let entries;
        try {
            entries = fs.readdirSync(dir, { withFileTypes: true });
        } catch (_) {
            return;
        }
        for (const entry of entries) {
            if (DIR_EXCLUDE.has(entry.name)) continue;
            const full = path.join(dir, entry.name);
            if (entry.isDirectory()) {
                walk(full);
                continue;
            }
            if (!entry.isFile()) continue;
            const ext = path.extname(entry.name).toLowerCase();
            if (!FILE_EXT_INCLUDE.has(ext)) continue;
            if (entry.name.endsWith('.min.js')) continue;
            if (entry.name.endsWith('.d.ts')) continue;
            files.push(full);
        }
    }

    for (const relative of CONSUMER_GLOB_DIRS) {
        walk(path.join(monorepoRoot, relative));
    }

    return files;
}

function findImportedEndpointNames(source, knownNames) {
    const importedAliases = new Map();
    const importRegex = /import\s*\{([^}]+)\}\s*from\s*['"](@rutba\/api-provider(?:\/[^'"]*)?)['"]/g;
    let m;
    while ((m = importRegex.exec(source)) !== null) {
        const namesChunk = m[1];
        const names = namesChunk.split(',').map((v) => v.trim()).filter(Boolean);
        for (const raw of names) {
            const aliasSplit = raw.split(/\s+as\s+/i);
            const original = aliasSplit[0].trim();
            const alias = (aliasSplit[1] ?? aliasSplit[0]).trim();
            if (knownNames.has(original)) {
                importedAliases.set(alias, original);
            }
        }
    }
    return importedAliases;
}

function scanFileForViolations(filePath, source, memberSets) {
    const knownNames = new Set(memberSets.keys());
    const imported = findImportedEndpointNames(source, knownNames);

    if (imported.size === 0) return [];

    const violations = [];

    for (const [alias, original] of imported) {
        const allowed = memberSets.get(original);
        if (!allowed) continue;

        const accessRegex = new RegExp(`\\b${alias}\\s*\\.\\s*([A-Za-z_$][\\w$]*)`, 'g');
        let match;
        while ((match = accessRegex.exec(source)) !== null) {
            const member = match[1];
            if (allowed.has(member)) continue;

            const upto = source.slice(0, match.index);
            const line = upto.split(/\r?\n/).length;
            const column = match.index - upto.lastIndexOf('\n');

            violations.push({
                file: filePath,
                line,
                column,
                endpointName: original,
                accessedAs: alias === original ? alias : `${alias} (alias for ${original})`,
                attemptedMember: member,
                suggestion: suggestClosest(member, allowed),
            });
        }
    }

    return violations;
}

function suggestClosest(member, allowed) {
    const lower = member.toLowerCase();
    let best = null;
    let bestScore = -Infinity;
    for (const candidate of allowed) {
        const cl = candidate.toLowerCase();
        let score = 0;
        if (cl === lower) score = 100;
        else if (cl.includes(lower) || lower.includes(cl)) score = 50;
        else {
            const common = longestCommonSubsequence(lower, cl);
            score = common - Math.abs(lower.length - cl.length);
        }
        if (score > bestScore) {
            bestScore = score;
            best = candidate;
        }
    }
    return bestScore > 0 ? best : null;
}

function longestCommonSubsequence(a, b) {
    const dp = Array.from({ length: a.length + 1 }, () => new Array(b.length + 1).fill(0));
    for (let i = 1; i <= a.length; i += 1) {
        for (let j = 1; j <= b.length; j += 1) {
            dp[i][j] = a[i - 1] === b[j - 1]
                ? dp[i - 1][j - 1] + 1
                : Math.max(dp[i - 1][j], dp[i][j - 1]);
        }
    }
    return dp[a.length][b.length];
}

function main() {
    const memberSets = readGeneratedMemberSets();
    if (memberSets.size === 0) {
        console.error('[validate-endpoint-usage] No generated .d.ts found. Run scaffold:endpoint-providers first.');
        process.exit(2);
    }

    const files = collectConsumerFiles();
    const allViolations = [];

    for (const file of files) {
        let source;
        try {
            source = fs.readFileSync(file, 'utf8');
        } catch (_) {
            continue;
        }
        if (!source.includes('@rutba/api-provider')) continue;

        const v = scanFileForViolations(file, source, memberSets);
        allViolations.push(...v);
    }

    const summary = {
        endpointsChecked: memberSets.size,
        consumerFilesScanned: files.length,
        violations: allViolations.length,
    };

    if (allViolations.length === 0) {
        console.log(`[validate-endpoint-usage] OK — ${summary.endpointsChecked} endpoint objects, ${summary.consumerFilesScanned} consumer files scanned, 0 violations.`);
        return;
    }

    const grouped = new Map();
    for (const v of allViolations) {
        const rel = path.relative(monorepoRoot, v.file).replace(/\\/g, '/');
        if (!grouped.has(rel)) grouped.set(rel, []);
        grouped.get(rel).push(v);
    }

    console.error(`[validate-endpoint-usage] FAIL — ${allViolations.length} violation(s) across ${grouped.size} file(s):\n`);

    for (const [rel, list] of [...grouped.entries()].sort(([a], [b]) => a.localeCompare(b))) {
        console.error(`  ${rel}`);
        for (const v of list) {
            const suggest = v.suggestion ? ` — did you mean "${v.suggestion}"?` : '';
            console.error(`    L${v.line}:C${v.column}  ${v.accessedAs}.${v.attemptedMember}${suggest}`);
        }
        console.error('');
    }

    console.error(`[validate-endpoint-usage] Source of truth: packages/api-provider/providers/generated/client/*.d.ts`);
    console.error('[validate-endpoint-usage] Regenerate via: npm run scaffold:endpoint-providers (inside packages/api-provider)');

    process.exit(1);
}

main();
